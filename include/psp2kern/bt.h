typedef struct {
	unsigned char mac[6];
	unsigned short unk0;
	unsigned int bt_class;
	unsigned int unk1;
	unsigned int unk2;
	unsigned short vid;
	unsigned short pid;
	unsigned int unk3;
	unsigned int unk4;
	char name[128];
	unsigned char unk5[0x60];
} SceBtRegisteredInfo; /* size = 0x100 */

typedef struct {
	// mac0 is at *(unsigned int *)&data[0x08];
	// mac1 is at *(unsigned int *)&data[0x0C];
	union {
		unsigned char data[0x10];
		struct {
			unsigned char id;
			unsigned char unk1;
			unsigned short unk2;
			unsigned int unk3;
			unsigned int mac0;
			unsigned int mac1;
		};
	};
} SceBtEvent;

typedef void (*SceBtCallback)(int r0, int r1, int r2, int r3);

typedef struct _SceBtHidRequest {
	uint32_t unk00;
	uint32_t unk04;
	uint8_t type; //@0x08, 0 = read?, 1 = write?, 2 = feature?, 3 = ?
	uint8_t unk09;
	uint8_t unk0A;
	uint8_t unk0B;
	void *buffer; // 0x0C
	uint32_t length; // 0x10
	struct _SceBtHidRequest *next;
} __attribute__((packed)) SceBtHidRequest; /* size = 0x18 */

